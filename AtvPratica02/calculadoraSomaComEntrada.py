"""
Este programa lê dois valores inteiros do usuário, soma-os e exibe o resultado.
"""

# Entrada de dados pelo usuário
# A função int() converte o texto digitado para um número inteiro.
A = int(input("Digite o primeiro valor inteiro: "))
B = int(input("Digite o segundo valor inteiro: "))

# Efetua a soma
X = A + B

# Imprime o resultado no formato especificado
print(f"X = {X}")

